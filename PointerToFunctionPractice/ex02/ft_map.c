#include <stdlib.h>

int		*ft_map(int *tab, int length, int (*f)(int))
{
	int i;

	i = 0;
	f = (malloc(sizeof(f) * length));
	while (i < length)
	{
		f(*(tab + i));
		i++;
	}
	return (tab);
}
