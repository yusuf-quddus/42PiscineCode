#include <stdlib.h>

char	*ft_strcpy(char *dest, char *src)
{
	int index;

	index = 0;
	while (src[index] != '\0')
	{
		dest[index] = src[index];
		index++;
	}
	dest[index] = '\0';
	return (dest);
}

int		ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

char	*ft_strdup(char *src)
{
	char *malptr;

	malptr = (char*)(malloc(sizeof(char) * (ft_strlen(src) + 1)));
	if (malptr == '\0')
	{
		return (0);
	}
	ft_strcpy(malptr, src);
	return (malptr);
}
